export function getTrustText(lang) {
  return trustDict[lang] || trustDict.en;
}

export function getTrustHreflang(page) {
  const languages = Object.keys(trustDict);
  const out = {};
  for (const lang of languages) out[lang] = `/${lang}/${page}`;
  out["x-default"] = `/en/${page}`;
  return out;
}
