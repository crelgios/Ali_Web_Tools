export function normalizeRoute(route = "home") {
  if (!route || route === "/" || route === "") return "home";
  return route.replace(/^\/+|\/+$/g, "") || "home";
}

export function getSeo(lang = "en", route = "home") {
  const safeLang = supportedLanguages.includes(lang) ? lang : "en";
  const safeRoute = normalizeRoute(route);
  return seoData[safeLang]?.[safeRoute] || seoData.en[safeRoute] || seoData.en.home;
}

export function getCanonical(lang = "en", route = "home") {
  const safeLang = supportedLanguages.includes(lang) ? lang : "en";
  const safeRoute = normalizeRoute(route);
  return safeRoute === "home" ? `${baseUrl}/${safeLang}` : `${baseUrl}/${safeLang}/${safeRoute}`;
}

export function getLanguageAlternates(route = "home") {
  const safeRoute = normalizeRoute(route);
  return Object.fromEntries(
    supportedLanguages.map((lang) => [
      lang,
      safeRoute === "home" ? `${baseUrl}/${lang}` : `${baseUrl}/${lang}/${safeRoute}`,
    ])
  );
}

export function createPageMetadata(lang = "en", route = "home") {
  const seo = getSeo(lang, route);
  return {
    title: seo.title,
    description: seo.description,
    keywords: seo.keywords,
    alternates: {
      canonical: getCanonical(lang, route),
      languages: getLanguageAlternates(route),
    },
    openGraph: {
      title: seo.title,
      description: seo.description,
      url: getCanonical(lang, route),
      siteName: "Ali W Vide",
      type: "website",
    },
  };
}
