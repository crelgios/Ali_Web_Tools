export function getText(lang) {
  return dict[lang] || dict.en;
}

export function getDir(lang) {
  const item = languageOptions.find((l) => l.code === lang);
  return item?.dir || "ltr";
}

export function getHreflang(tool) {
  const out = {};
  for (const language of languageOptions) {
    out[language.code] = `/${language.code}/${tool}`;
  }
  out["x-default"] = `/en/${tool}`;
  return out;
}
